import Foundation

class AccountInfo{
    var firstName: String
    var lastName: String
    var email: String
    var age: String
    
    init(firstName: String, lastName: String, email: String, age: String){
        self.firstName = firstName
        self.lastName = lastName
        self.email = email
        self.age = age
    }
    
    
    
}
